#!/usr/bin/env node
/**
 * Build a portable SearXNG virtualenv using the bundled Python runtime.
 * Source is copied from vendor/searxng-src (no git clone in CI).
 */
import { cpSync, existsSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { execSync } from 'node:child_process';

const root = resolve(import.meta.dirname, '..');
const out = resolve(root, 'src-tauri/bundle-runtime');
const vendorDir = join(root, 'vendor/searxng-src');
const pythonRoot = join(out, 'python');
const venvDir = join(out, 'searxng-venv');
const srcDir = join(out, 'searxng-src');

/** Not needed at runtime for `python -m searx.webapp`. */
const PRUNE_DIRS = [
  'utils',
  '.github',
  'client',
  '.devcontainer',
  '.vscode',
  'docs',
  'container',
  'tests',
];

function pythonBin() {
  if (process.platform === 'win32') {
    return join(pythonRoot, 'python.exe');
  }
  const candidates = [
    join(pythonRoot, 'bin', 'python3'),
    join(pythonRoot, 'bin', 'python'),
    join(pythonRoot, 'python')
  ];
  for (const p of candidates) {
    if (existsSync(p)) return p;
  }
  throw new Error(`Bundled Python not found under ${pythonRoot}`);
}

function venvPython() {
  if (process.platform === 'win32') {
    return join(venvDir, 'Scripts', 'python.exe');
  }
  return join(venvDir, 'bin', 'python');
}

function run(cmd, opts = {}) {
  execSync(cmd, {
    stdio: 'inherit',
    shell: process.platform === 'win32',
    ...opts
  });
}

function hasVendoredSearxng() {
  return (
    existsSync(join(vendorDir, 'searx', 'webapp.py')) ||
    existsSync(join(vendorDir, 'setup.py')) ||
    existsSync(join(vendorDir, 'pyproject.toml'))
  );
}

function stageSearxngSource() {
  if (!hasVendoredSearxng()) {
    throw new Error(
      'vendor/searxng-src is missing. On Linux/macOS run: npm run vendor:searxng'
    );
  }

  const revPath = join(vendorDir, 'VENDOR_REVISION');
  if (existsSync(revPath)) {
    console.log('Using vendored SearXNG', readFileSync(revPath, 'utf8').trim());
  } else {
    console.log('Using vendored SearXNG from', vendorDir);
  }

  rmSync(srcDir, { recursive: true, force: true });
  cpSync(vendorDir, srcDir, { recursive: true });
}

function pruneRuntimeTree() {
  for (const dir of PRUNE_DIRS) {
    const target = join(srcDir, dir);
    if (existsSync(target)) {
      rmSync(target, { recursive: true, force: true });
      console.log('Pruned from bundle searxng-src:', dir);
    }
  }
}

function writeVersionFrozen() {
  const revPath = join(srcDir, 'VENDOR_REVISION');
  let rev = 'vendor';
  if (existsSync(revPath)) {
    const raw = readFileSync(revPath, 'utf8').trim();
    const parts = raw.split(/\s+/);
    rev = parts.length > 1 ? parts[1].slice(0, 12) : parts[0].slice(0, 12);
  }

  // PEP 440-compliant version required by setuptools/packaging.
  // Keep revision in local version segment so editable install succeeds.
  const safeRev = rev.toLowerCase().replace(/[^a-z0-9.]+/g, '.');
  const version = `0+visionos.${safeRev}`;
  const dockerTag = version.replaceAll('+', '-');
  const content = `# SPDX-License-Identifier: AGPL-3.0-or-later
# Generated by scripts/prepare-searxng-python.mjs (no git required at runtime)

VERSION_STRING = "${version}"
VERSION_TAG = "${version}"
DOCKER_TAG = "${dockerTag}"
GIT_URL = "https://github.com/searxng/searxng"
GIT_BRANCH = "vendor"
`;
  writeFileSync(join(srcDir, 'searx', 'version_frozen.py'), content, 'utf8');
  console.log('Wrote searx/version_frozen.py for', version);
}

/** Command engines are Unix-oriented; disable on Windows bundled runtime. */
function patchCommandEngineForWindows() {
  if (process.platform !== 'win32') return;

  const file = join(srcDir, 'searx', 'engines', 'command.py');
  let text = readFileSync(file, 'utf8');
  if (text.includes('VISIONOS_WINDOWS_GUARD')) return;

  const pattern = /def search\(query, params\) -> EngineResults:\r?\n(\s+)res = EngineResults\(\)/;
  const replacement =
    'def search(query, params) -> EngineResults:\n' +
    "    import os\n" +
    "    if os.name == 'nt':  # VISIONOS_WINDOWS_GUARD\n" +
    '        return EngineResults()\n' +
    '    res = EngineResults()';

  if (!pattern.test(text)) {
    console.warn('Could not patch command.py for Windows (upstream changed)');
    return;
  }

  writeFileSync(file, text.replace(pattern, replacement), 'utf8');
  console.log('Patched searx/engines/command.py for Windows');
}

function main() {
  if (!existsSync(pythonRoot)) {
    throw new Error('Run npm run prepare:python before prepare:searxng-python');
  }

  const py = pythonBin();
  console.log('Creating SearXNG virtualenv…');
  rmSync(venvDir, { recursive: true, force: true });
  run(`"${py}" -m venv "${venvDir}"`);

  const vpy = venvPython();
  run(`"${vpy}" -m pip install -U pip setuptools wheel`);
  run(
    `"${vpy}" -m pip install -U pyyaml msgspec typing-extensions pybind11 babel jinja2 flask`
  );
  if (process.platform === 'win32') {
    run(`"${vpy}" -m pip install -U lxml`);
  }

  stageSearxngSource();
  pruneRuntimeTree();
  writeVersionFrozen();
  patchCommandEngineForWindows();
  run(`"${vpy}" -m pip install --use-pep517 --no-build-isolation .`, { cwd: srcDir });

  console.log('SearXNG virtualenv ready at', venvDir);
}

main();
